/**
 * Model representing a registration
 */
export class Registration {

  /**
   * The object type
   */
  type: string;

  /**
   * The email linked to the registration
   */
  email: string;

  /**
   * The user linked to the registration
   */
  user: string;

  /**
   * The token linked to the registration
   */
  token: string;

}
